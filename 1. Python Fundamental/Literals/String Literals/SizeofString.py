""" String Literals """
# Size of String

'''Note: len() function in Python is a built-in function used to determine the length of an object. It returns the number of items in a given object such as a string, list, tuple, dictionary, set, or other collection types.'''

# Single line String
a='\\'
print(len(a))

a="\ab"
print(len(a))

a="raj\'s phone"
print(len(a))

a="KSK's"
print(len(a))


# Multi line String

# Multi line String created with triple quote(''' or """)
str='''a
b
c'''
print(len(str))

# Multi line String created with single or double quote(" or ')
str1="a\
b\
c"
print(len(str1))