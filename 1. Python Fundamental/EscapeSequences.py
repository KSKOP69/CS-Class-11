''' String Literals '''
# Escape Sequences

# \\ Blackslash
a='Hello \\ World'
print(a)

# \' Single quote
a='Hello \' World'
print(a)

# \" Double quote
a="Hello \" World"
print(a)

# \a Ascii bell
a="Hello\a World"
print(a)

# \b Backspace
a="Hello\b World"
print(a)

# \n New Line
a="Hello\n World"
print(a)

# \r Carriage Return
a="Hello\rWorld"
print(a)

# \t Tab
a="Hello\tWorld"
print(a)