''' String Literals '''
# Types of String

# Single line String
a="Hello Welcome to KSK Programming"
print(a)

# Multi line String

# 1st Method (' or ")
a='Hello Welcome to KSK \
Programming.Python is very easy \
to learn langauge.'
print(a)

# 2nd Method (""" or ''')
a="""Hello Welcome to KSK
Programming.Python is very easy
to learn langauge."""
print(a)