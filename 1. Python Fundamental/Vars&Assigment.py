""" Variable and Assigment """

'''
Variable: A variable is a named storage location in a computer program where you can store data. Variables allow programmers to manipulate and reuse data in their programs.

Assignment: Assignment is the process of assigning a value to a variable. It uses the assignment operator (=).
'''
# a,b,c are variables
a=5
b=15
c=a+b
print(c)


# Multiple Assigment

# Same value to mutiple variables
a=b=c=5
print(a)
print(b)
print(c)

# Different values to mutiple variables
a,b,c=5,15,25
print(a)
print(b)
print(c)